public class Break_Statement {
   public static void main(String[] args) {
    for(int i=1;i<=5;i++){
        System.out.println(i);
        System.out.println("java is great.");
        if(i==3){
            System.out.println("ending the loop.");
            break;
        }
    }
   } 
}
